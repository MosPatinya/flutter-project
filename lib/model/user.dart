class User {
  String name;
  String name1;
  String name2;
  String name3;
  String ename;
  String cata;
  String type;
  String phone;
  String timeopen;
  String timeclose;
  String price;
  String web;
  String facebook;

  User({
    String name,
    String name1,
    String name2,
    String name3,
    String ename,
    String cata,
    String type,
    String phone,
    String timeopen,
    String timeclose,
    String price,
    String web,
    String facebook,
  }) {
    this.name = name;
    this.name1 = name1;
    this.name2 = name2;
    this.name3 = name3;
    this.ename = ename;
    this.cata = cata;
    this.type = type;
    this.phone = phone;
    this.timeopen = timeopen;
    this.timeclose = timeclose;
    this.price = price;
    this.web = web;
    this.facebook = name;
  }

}
